"""
export_weights.py — Convert a trained PortraitGAN generator checkpoint
into a JavaScript-embeddable base64 Float32Array literal.

Usage:
    # Full float32 export (~15 MB JS string):
    python export/export_weights.py \
        --checkpoint run/checkpoints/generator_final.pth \
        --output inference/weights_b64.js

    # Int8 quantized export (~4 MB JS string, slight quality loss):
    python export/export_weights.py \
        --checkpoint run/checkpoints/generator_final.pth \
        --output inference/weights_b64.js \
        --quantize int8

    # Then build final HTML:
    python export/build_html.py \
        --weights inference/weights_b64.js \
        --output docs/portrait_gan.html

Output format (weights_b64.js):
    // Auto-generated by export_weights.py
    // Generator: 3,812,099 parameters | float32 | 14.5 MB raw | 4.2 MB quantized
    const MODEL_METADATA = { ... };
    const WEIGHTS_B64 = "...base64 string...";
    const QUANT_SCALE = [...];   // only if quantized
    const QUANT_ZERO  = [...];   // only if quantized
"""

